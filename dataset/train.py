"""OpGAN training script.

Trains a 1D Operational GAN to denoise analog classical music recordings
using paired clean/noisy data generated by the dataset pipeline.

Usage:
    python -m dataset.train --pairs data/pairs/ --epochs 100
    python -m dataset.train --pairs data/pairs/ --epochs 50 --batch-size 16 --lr 1e-4
    python -m dataset.train --pairs data/pairs/ --resume checkpoints/best_generator.pt

Requires:
    pip install -e ".[dataset]"
"""

from __future__ import annotations

import argparse
import json
import logging
import sys
import time
from pathlib import Path

import torch
from torch.utils.data import DataLoader, random_split

# Import OpGAN components from archive
sys.path.insert(0, str(Path(__file__).resolve().parent.parent / "backend"))
from app._archive.op_gan import (  # noqa: E402
    GradientHealthMonitor,
    OpGANDiscriminator,
    OpGANGenerator,
    OpGANLoss,
)

from dataset.torch_dataset import AnalogAudioDataset  # noqa: E402

logger = logging.getLogger(__name__)

# Training defaults
_DEFAULT_BATCH_SIZE = 8
_DEFAULT_EPOCHS = 100
_DEFAULT_LR_GEN = 2e-4
_DEFAULT_LR_DISC = 2e-4
_DEFAULT_VAL_SPLIT = 0.1
_DEFAULT_CHECKPOINT_DIR = Path("checkpoints")
_DEFAULT_LOG_INTERVAL = 10
_MAX_GRAD_NORM = 1.0


def _select_device() -> torch.device:
    """Select the best available device."""
    if torch.cuda.is_available():
        device = torch.device("cuda")
        logger.info("Using CUDA: %s", torch.cuda.get_device_name(0))
    elif hasattr(torch.backends, "mps") and torch.backends.mps.is_available():
        device = torch.device("mps")
        logger.info("Using Apple MPS")
    else:
        device = torch.device("cpu")
        logger.info("Using CPU")
    return device


def _save_checkpoint(
    path: Path,
    generator: OpGANGenerator,
    discriminator: OpGANDiscriminator,
    optimizer_g: torch.optim.Optimizer,
    optimizer_d: torch.optim.Optimizer,
    epoch: int,
    best_val_loss: float,
) -> None:
    """Save training checkpoint."""
    path.parent.mkdir(parents=True, exist_ok=True)
    torch.save(
        {
            "epoch": epoch,
            "generator_state_dict": generator.state_dict(),
            "discriminator_state_dict": discriminator.state_dict(),
            "optimizer_g_state_dict": optimizer_g.state_dict(),
            "optimizer_d_state_dict": optimizer_d.state_dict(),
            "best_val_loss": best_val_loss,
        },
        path,
    )
    logger.info("Saved checkpoint: %s (epoch %d)", path, epoch)


def _load_checkpoint(
    path: Path,
    generator: OpGANGenerator,
    discriminator: OpGANDiscriminator,
    optimizer_g: torch.optim.Optimizer,
    optimizer_d: torch.optim.Optimizer,
    device: torch.device,
) -> tuple[int, float]:
    """Load training checkpoint.

    Returns:
        Tuple of (start_epoch, best_val_loss).
    """
    checkpoint = torch.load(path, map_location=device, weights_only=True)
    generator.load_state_dict(checkpoint["generator_state_dict"])
    discriminator.load_state_dict(checkpoint["discriminator_state_dict"])
    optimizer_g.load_state_dict(checkpoint["optimizer_g_state_dict"])
    optimizer_d.load_state_dict(checkpoint["optimizer_d_state_dict"])
    epoch = checkpoint["epoch"]
    best_val_loss = checkpoint["best_val_loss"]
    logger.info("Resumed from checkpoint: %s (epoch %d, best_val=%.4f)", path, epoch, best_val_loss)
    return epoch, best_val_loss


def train_epoch(
    generator: OpGANGenerator,
    discriminator: OpGANDiscriminator,
    criterion: OpGANLoss,
    optimizer_g: torch.optim.Optimizer,
    optimizer_d: torch.optim.Optimizer,
    dataloader: DataLoader,
    device: torch.device,
    grad_monitor: GradientHealthMonitor,
    scaler: torch.amp.GradScaler | None = None,
) -> dict[str, float]:
    """Run one training epoch.

    Args:
        generator: OpGAN generator model.
        discriminator: OpGAN discriminator model.
        criterion: Combined loss function.
        optimizer_g: Generator optimizer.
        optimizer_d: Discriminator optimizer.
        dataloader: Training data loader.
        device: Torch device.
        grad_monitor: Gradient health monitor for clipping.
        scaler: GradScaler for CUDA mixed precision (None disables).

    Returns:
        Dict of average losses for the epoch.
    """
    generator.train()
    discriminator.train()

    use_amp = scaler is not None
    totals = {"g_total": 0.0, "g_adv": 0.0, "g_temp": 0.0, "g_spec": 0.0, "d_loss": 0.0}
    num_batches = 0

    for noisy, clean in dataloader:
        noisy = noisy.to(device)
        clean = clean.to(device)

        # --- Discriminator step ---
        with torch.amp.autocast(device.type, enabled=use_amp):
            generated = generator(noisy)
            disc_real = discriminator(clean)
            disc_fake = discriminator(generated.detach())
            d_loss = criterion.discriminator_loss(disc_real, disc_fake)

        optimizer_d.zero_grad()
        if scaler:
            scaler.scale(d_loss).backward()
            scaler.unscale_(optimizer_d)
            torch.nn.utils.clip_grad_norm_(discriminator.parameters(), _MAX_GRAD_NORM)
            scaler.step(optimizer_d)
        else:
            d_loss.backward()
            torch.nn.utils.clip_grad_norm_(discriminator.parameters(), _MAX_GRAD_NORM)
            optimizer_d.step()

        # --- Generator step ---
        with torch.amp.autocast(device.type, enabled=use_amp):
            disc_fake_for_gen = discriminator(generated)
            g_loss, breakdown = criterion.generator_loss(disc_fake_for_gen, generated, clean)

        optimizer_g.zero_grad()
        if scaler:
            scaler.scale(g_loss).backward()
            scaler.unscale_(optimizer_g)
            grad_monitor.check_and_clip_gradients(generator, "gen")
            scaler.step(optimizer_g)
            scaler.update()
        else:
            g_loss.backward()
            grad_monitor.check_and_clip_gradients(generator, "gen")
            optimizer_g.step()

        totals["g_total"] += breakdown["total"]
        totals["g_adv"] += breakdown["adversarial"]
        totals["g_temp"] += breakdown["temporal"]
        totals["g_spec"] += breakdown["spectral"]
        totals["d_loss"] += d_loss.item()
        num_batches += 1

    return {k: v / max(1, num_batches) for k, v in totals.items()}


@torch.no_grad()
def validate(
    generator: OpGANGenerator,
    discriminator: OpGANDiscriminator,
    criterion: OpGANLoss,
    dataloader: DataLoader,
    device: torch.device,
) -> dict[str, float]:
    """Run validation.

    Returns:
        Dict of average validation losses.
    """
    generator.eval()
    discriminator.eval()

    totals = {"g_total": 0.0, "g_adv": 0.0, "g_temp": 0.0, "g_spec": 0.0, "d_loss": 0.0}
    num_batches = 0

    for noisy, clean in dataloader:
        noisy = noisy.to(device)
        clean = clean.to(device)

        generated = generator(noisy)

        disc_real = discriminator(clean)
        disc_fake = discriminator(generated)
        d_loss = criterion.discriminator_loss(disc_real, disc_fake)

        disc_for_gen = discriminator(generated)
        _, breakdown = criterion.generator_loss(disc_for_gen, generated, clean)

        totals["g_total"] += breakdown["total"]
        totals["g_adv"] += breakdown["adversarial"]
        totals["g_temp"] += breakdown["temporal"]
        totals["g_spec"] += breakdown["spectral"]
        totals["d_loss"] += d_loss.item()
        num_batches += 1

    return {k: v / max(1, num_batches) for k, v in totals.items()}


def train(
    pairs_dir: Path,
    epochs: int = _DEFAULT_EPOCHS,
    batch_size: int = _DEFAULT_BATCH_SIZE,
    lr_gen: float = _DEFAULT_LR_GEN,
    lr_disc: float = _DEFAULT_LR_DISC,
    val_split: float = _DEFAULT_VAL_SPLIT,
    checkpoint_dir: Path = _DEFAULT_CHECKPOINT_DIR,
    resume_path: Path | None = None,
    log_interval: int = _DEFAULT_LOG_INTERVAL,
) -> None:
    """Train the OpGAN denoiser.

    Args:
        pairs_dir: Directory containing clean/ and noisy/ subdirs.
        epochs: Number of training epochs.
        batch_size: Batch size for training.
        lr_gen: Learning rate for generator.
        lr_disc: Learning rate for discriminator.
        val_split: Fraction of data for validation.
        checkpoint_dir: Directory to save checkpoints.
        resume_path: Path to checkpoint to resume from.
        log_interval: Log every N epochs.
    """
    device = _select_device()

    # --- Data ---
    dataset = AnalogAudioDataset(pairs_dir)
    stats = dataset.get_stats()
    logger.info(
        "Dataset: %d pairs, %d clean frames, %.1f variants/frame",
        stats["total_pairs"],
        stats["clean_frames"],
        stats["variants_per_frame"],
    )

    val_size = int(len(dataset) * val_split)
    train_size = len(dataset) - val_size
    train_dataset, val_dataset = random_split(
        dataset,
        [train_size, val_size],
        generator=torch.Generator().manual_seed(42),
    )

    # pin_memory only works on CUDA, not MPS or CPU
    use_pin_memory = device.type == "cuda"
    train_loader = DataLoader(
        train_dataset,
        batch_size=batch_size,
        shuffle=True,
        num_workers=2,
        pin_memory=use_pin_memory,
        drop_last=True,
    )
    val_loader = DataLoader(
        val_dataset,
        batch_size=batch_size,
        shuffle=False,
        num_workers=2,
        pin_memory=use_pin_memory,
    )

    logger.info(
        "Train: %d pairs (%d batches), Val: %d pairs", train_size, len(train_loader), val_size
    )

    # --- Models ---
    generator = OpGANGenerator(input_length=32000, q=3).to(device)
    discriminator = OpGANDiscriminator(input_length=32000, q=2).to(device)
    criterion = OpGANLoss(lambda_temporal=10, lambda_spectral=5)

    gen_params = sum(p.numel() for p in generator.parameters())
    disc_params = sum(p.numel() for p in discriminator.parameters())
    logger.info(
        "Generator: %s params, Discriminator: %s params", f"{gen_params:,}", f"{disc_params:,}"
    )

    # --- Optimizers ---
    optimizer_g = torch.optim.AdamW(generator.parameters(), lr=lr_gen, betas=(0.5, 0.999))
    optimizer_d = torch.optim.AdamW(discriminator.parameters(), lr=lr_disc, betas=(0.5, 0.999))

    # --- Resume ---
    start_epoch = 0
    best_val_loss = float("inf")
    if resume_path and resume_path.exists():
        start_epoch, best_val_loss = _load_checkpoint(
            resume_path,
            generator,
            discriminator,
            optimizer_g,
            optimizer_d,
            device,
        )

    # --- Training ---
    grad_monitor = GradientHealthMonitor(max_grad_norm=_MAX_GRAD_NORM, log_frequency=100)
    checkpoint_dir.mkdir(parents=True, exist_ok=True)

    # Mixed precision: only on CUDA (MPS doesn't support float16 autocast)
    use_amp = device.type == "cuda"
    scaler = torch.amp.GradScaler("cuda") if use_amp else None
    if use_amp:
        logger.info("CUDA AMP enabled (float16 mixed precision)")

    # Save training config for reproducibility
    config = {
        "pairs_dir": str(pairs_dir),
        "epochs": epochs,
        "batch_size": batch_size,
        "lr_gen": lr_gen,
        "lr_disc": lr_disc,
        "val_split": val_split,
        "train_size": train_size,
        "val_size": val_size,
        "device": str(device),
        "gen_params": gen_params,
        "disc_params": disc_params,
        "mixed_precision": use_amp,
    }
    config_path = checkpoint_dir / "train_config.json"
    with open(config_path, "w") as f:
        json.dump(config, f, indent=2)

    logger.info("Starting training for %d epochs (from epoch %d)", epochs, start_epoch)

    for epoch in range(start_epoch, epochs):
        epoch_start = time.time()

        train_losses = train_epoch(
            generator,
            discriminator,
            criterion,
            optimizer_g,
            optimizer_d,
            train_loader,
            device,
            grad_monitor,
            scaler,
        )

        val_losses = validate(
            generator,
            discriminator,
            criterion,
            val_loader,
            device,
        )

        epoch_time = time.time() - epoch_start

        # Log progress
        if (epoch + 1) % log_interval == 0 or epoch == start_epoch:
            logger.info(
                "Epoch %d/%d (%.1fs) -- "
                "Train: G=%.4f (adv=%.4f, temp=%.4f, spec=%.4f) D=%.4f -- "
                "Val: G=%.4f D=%.4f",
                epoch + 1,
                epochs,
                epoch_time,
                train_losses["g_total"],
                train_losses["g_adv"],
                train_losses["g_temp"],
                train_losses["g_spec"],
                train_losses["d_loss"],
                val_losses["g_total"],
                val_losses["d_loss"],
            )

        # Save best model
        if val_losses["g_total"] < best_val_loss:
            best_val_loss = val_losses["g_total"]
            _save_checkpoint(
                checkpoint_dir / "best.pt",
                generator,
                discriminator,
                optimizer_g,
                optimizer_d,
                epoch + 1,
                best_val_loss,
            )

        # Periodic checkpoint every 10 epochs
        if (epoch + 1) % 10 == 0:
            _save_checkpoint(
                checkpoint_dir / f"epoch_{epoch + 1:04d}.pt",
                generator,
                discriminator,
                optimizer_g,
                optimizer_d,
                epoch + 1,
                best_val_loss,
            )

    # Final save
    _save_checkpoint(
        checkpoint_dir / "final.pt",
        generator,
        discriminator,
        optimizer_g,
        optimizer_d,
        epochs,
        best_val_loss,
    )

    # Log gradient stats
    grad_stats = grad_monitor.get_gradient_stats()
    if grad_stats:
        logger.info(
            "Gradient stats: mean=%.2e, max=%.2e, min=%.2e",
            grad_stats["mean_norm"],
            grad_stats["max_norm"],
            grad_stats["min_norm"],
        )

    logger.info("Training complete. Best val loss: %.4f", best_val_loss)


def main() -> None:
    """CLI entry point."""
    parser = argparse.ArgumentParser(
        description="Train the OpGAN denoiser on paired audio data.",
    )
    parser.add_argument(
        "--pairs",
        type=Path,
        default=Path("data/pairs"),
        help="Directory containing clean/ and noisy/ subdirs (default: data/pairs/)",
    )
    parser.add_argument(
        "--epochs",
        type=int,
        default=_DEFAULT_EPOCHS,
        help=f"Number of training epochs (default: {_DEFAULT_EPOCHS})",
    )
    parser.add_argument(
        "--batch-size",
        type=int,
        default=_DEFAULT_BATCH_SIZE,
        help=f"Batch size (default: {_DEFAULT_BATCH_SIZE})",
    )
    parser.add_argument(
        "--lr-gen",
        type=float,
        default=_DEFAULT_LR_GEN,
        help=f"Generator learning rate (default: {_DEFAULT_LR_GEN})",
    )
    parser.add_argument(
        "--lr-disc",
        type=float,
        default=_DEFAULT_LR_DISC,
        help=f"Discriminator learning rate (default: {_DEFAULT_LR_DISC})",
    )
    parser.add_argument(
        "--val-split",
        type=float,
        default=_DEFAULT_VAL_SPLIT,
        help=f"Validation split fraction (default: {_DEFAULT_VAL_SPLIT})",
    )
    parser.add_argument(
        "--checkpoint-dir",
        type=Path,
        default=_DEFAULT_CHECKPOINT_DIR,
        help=f"Checkpoint directory (default: {_DEFAULT_CHECKPOINT_DIR})",
    )
    parser.add_argument(
        "--resume",
        type=Path,
        default=None,
        help="Resume from checkpoint file",
    )
    parser.add_argument(
        "--log-interval",
        type=int,
        default=_DEFAULT_LOG_INTERVAL,
        help=f"Log every N epochs (default: {_DEFAULT_LOG_INTERVAL})",
    )

    args = parser.parse_args()

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s - %(name)s - %(levelname)s - %(message)s",
    )

    train(
        pairs_dir=args.pairs,
        epochs=args.epochs,
        batch_size=args.batch_size,
        lr_gen=args.lr_gen,
        lr_disc=args.lr_disc,
        val_split=args.val_split,
        checkpoint_dir=args.checkpoint_dir,
        resume_path=args.resume,
        log_interval=args.log_interval,
    )


if __name__ == "__main__":
    main()
