"""PyTorch Dataset for paired noisy/clean audio frames.

Loads training pairs generated by generate_pairs.py and returns
(noisy, clean) tensor pairs ready for the OpGAN generator.

Usage:
    from dataset.torch_dataset import AnalogAudioDataset
    from torch.utils.data import DataLoader

    dataset = AnalogAudioDataset(Path("data/pairs"))
    loader = DataLoader(dataset, batch_size=8, shuffle=True)

    for noisy, clean in loader:
        # noisy: [batch, 1, 32000]
        # clean: [batch, 1, 32000]
        output = generator(noisy)
        loss = criterion(output, clean)
"""

from __future__ import annotations

import logging
from pathlib import Path

import numpy as np
import soundfile as sf
import torch
from torch.utils.data import Dataset

logger = logging.getLogger(__name__)


class AnalogAudioDataset(Dataset):
    """Dataset of paired noisy/clean audio frames for OpGAN training.

    Expects the directory structure created by generate_pairs.py:
        pairs_dir/
            clean/frame_name.wav
            noisy/frame_name__v00.wav
            noisy/frame_name__v01.wav
            ...

    Each noisy file maps to its clean counterpart via the filename
    prefix (everything before "__v").

    Args:
        pairs_dir: Root directory containing clean/ and noisy/ subdirs.
        transform: Optional transform applied to both noisy and clean tensors.
    """

    def __init__(
        self,
        pairs_dir: Path,
        transform: torch.nn.Module | None = None,
    ) -> None:
        self.pairs_dir = Path(pairs_dir)
        self.clean_dir = self.pairs_dir / "clean"
        self.noisy_dir = self.pairs_dir / "noisy"
        self.transform = transform

        if not self.clean_dir.exists():
            raise FileNotFoundError(f"Clean directory not found: {self.clean_dir}")
        if not self.noisy_dir.exists():
            raise FileNotFoundError(f"Noisy directory not found: {self.noisy_dir}")

        # Build index: list of (noisy_path, clean_path) pairs
        self._pairs = self._build_index()

        if not self._pairs:
            raise ValueError(f"No valid pairs found in {pairs_dir}")

        logger.info(
            "Loaded %d pairs from %s (%d clean frames)",
            len(self._pairs),
            pairs_dir,
            len(set(p[1] for p in self._pairs)),
        )

    def _build_index(self) -> list[tuple[Path, Path]]:
        """Build list of (noisy_path, clean_path) pairs."""
        pairs = []

        # Map clean stems for lookup
        clean_files = {f.stem: f for f in self.clean_dir.glob("*.wav")}

        for noisy_path in sorted(self.noisy_dir.glob("*.wav")):
            # Extract clean frame name: "frame_name__v00" -> "frame_name"
            noisy_stem = noisy_path.stem
            separator_idx = noisy_stem.rfind("__v")
            if separator_idx == -1:
                logger.warning("Unexpected filename format: %s", noisy_path.name)
                continue

            clean_stem = noisy_stem[:separator_idx]

            if clean_stem not in clean_files:
                logger.warning(
                    "Clean file not found for %s (expected %s.wav)",
                    noisy_path.name, clean_stem,
                )
                continue

            pairs.append((noisy_path, clean_files[clean_stem]))

        return pairs

    def __len__(self) -> int:
        return len(self._pairs)

    def __getitem__(self, idx: int) -> tuple[torch.Tensor, torch.Tensor]:
        """Load a noisy/clean pair as tensors.

        Returns:
            Tuple of (noisy, clean) tensors, each shaped [1, frame_len].
        """
        noisy_path, clean_path = self._pairs[idx]

        noisy_audio = self._load_wav(noisy_path)
        clean_audio = self._load_wav(clean_path)

        # Add channel dimension: [frame_len] -> [1, frame_len]
        noisy = torch.from_numpy(noisy_audio).unsqueeze(0)
        clean = torch.from_numpy(clean_audio).unsqueeze(0)

        if self.transform is not None:
            noisy = self.transform(noisy)
            clean = self.transform(clean)

        return noisy, clean

    @staticmethod
    def _load_wav(path: Path) -> np.ndarray:
        """Load a .wav file as float32 numpy array."""
        audio, _ = sf.read(path, dtype="float32")
        return audio

    def get_clean_frame_count(self) -> int:
        """Return number of unique clean frames in the dataset."""
        return len(set(p[1] for p in self._pairs))

    def get_stats(self) -> dict[str, int | float]:
        """Return dataset statistics."""
        clean_count = self.get_clean_frame_count()
        return {
            "total_pairs": len(self._pairs),
            "clean_frames": clean_count,
            "variants_per_frame": len(self._pairs) / max(1, clean_count),
        }
